package com.smartit.jumpcloud;

public class ActionAvg {

    private String action;

    private Integer avg;

    public ActionAvg() {
    }

    public ActionAvg(String action, Integer avg) {
        this.action = action;
        this.avg = avg;
    }

    public String getAction() {
        return action;
    }

    public Integer getAvg() {
        return avg;
    }

}
